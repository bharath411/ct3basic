package naveenkumarkb;

public class Loops {
	public static void main(String args[]) {
		for (int i=1; i<3;i++) {
			System.out.println("For Increment Loop");
	}
	int j=1;
	while(j<3) {
		System.out.println("While Increment Loop");
		j++;
	}
	for(int k=3;k>1;k--) {
		System.out.println("For Decrement Loop");
	}
	int l=3;
	while(l>1) {
		System.out.println("While Decrement Loop");
		l--;
	}
	
	}
}
