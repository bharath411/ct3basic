package basic;

public class LoopExample {

	
	public void test1() {
		
		//for
		
		for(int i =0; i<10;i++) {
			System.out.println("bharath");
		}
		for(int i =13; i>0;i--) {
			System.out.println("bharath");
		}
		
		// While
		int j=0;
		while(j<10) {
			System.out.println("bharath");
			j++;
		}
		
	}
}
